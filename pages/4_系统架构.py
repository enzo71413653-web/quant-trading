st.header("③ 为什么现在不做「无头自动交易引擎」")
st.markdown("""
你提的"事件驱动引擎 + 券商API对接"在架构上没有错——专业机构确实这么搭。但三个具体原因，让**现在**做这件事弊大于利：

1. **纸面盘≈实盘的钥匙**：Alpaca / IBKR 这类API，paper 和 live 账户往往共用同一套下单接口，通常只是一个账户类型标志位的区别。
   今天写的 `execution.py`，明天只要改一个配置就能对接真实账户——这把"钥匙"不该在没有配套的严谨测试和风控之前就焊进代码里。
2. **生产级容错不是一次写完的**：断线重连、重复下单防护、限流退避、非交易时段/时区边界——这类系统历史上真实发生过灾难性事故
   （如 Knight Capital 2012年因一次有缺陷的自动化交易部署，45分钟内损失 4.4 亿美元）。一次性写出"看起来能跑"的版本，
   比不写风险更大，因为它会给你虚假的信心。
3. **部署环境本身不支持**：Streamlit Community Cloud 没有人访问网页时不执行任何代码，物理上跑不了 `while True` 常驻进程。
   真要做，需要单独的云主机/常驻服务——这是一个新的基础设施决策，不是给现有网页加一页能解决的。
""")

st.header("④ 如果以后真想往这个方向走——正确的路径")
st.markdown("""
不是"一步到位一个自循环引擎"，而是每一步都可独立验证、可人工监督：

1. 你自己开一个 Alpaca（或类似）**纸面交易账户**，生成你自己的 API Key（这一步必须是你本人操作，我不能替你开户）
2. 第一个功能只做**只读**：拉取你纸面账户的当前持仓/余额，验证对得上——不涉及下单
3. 再加**单笔手动触发**的下单函数，每次下单前打印完整参数、等你在终端里按确认键才真正发送
4. 用这个"人工监督下单"模式跑够长时间，观察网络延迟、限流、非交易时段报错等边界情况怎么处理
5. 只有在④稳定很久之后，才考虑要不要把"确认"这一步自动化——而且即便到那时，也建议保留一个总开关和单笔金额上限

这条路径比"直接写事件循环"慢，但每一步出错的代价都可控。
""")

